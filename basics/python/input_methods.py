# 入力の基本（input vs sys.stdin.readline）

# ───────────────────────────────────────────────────────────────
# input(): Python 標準の入力関数。1 行の入力を受け取って文字列で返す。
# - 特徴：わかりやすい。初心者向け。インタラクティブな入力に向く。
# - 欠点：大量入力の際は遅い

# sys.stdin.readline(): 入力ストリームから直接1行取得。末尾に改行 '\n' を含むので注意。
# - 特徴：高速。大規模データ処理で有利。
# - 欠点：int() 変換時に strip() が必要なことがある。くらい(?)

# 使用例：

import sys

# 基本的な使い方（strip()で改行除去）
line = sys.stdin.readline().strip()
numbers = list(map(int, line.split()))

# 複数行を読み込む
n = int(sys.stdin.readline())
for _ in range(n):
    data = list(map(int, sys.stdin.readline().split()))

# ───────────────────────────────────────────────────────────────
# 【使い分けの目安】
# - 少量入力：input()
# - 多量入力：sys.stdin.readline() + strip()
# ───────────────────────────────────────────────────────────────

# 入力例（標準入力）:
# 3
# 10 20 30
# 40 50 60
# 70 80 90

# 出力例:
# [10, 20, 30]
# [40, 50, 60]
# [70, 80, 90]
