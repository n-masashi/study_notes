# リスト（変更可な配列）
fruits = ['apple', 'banana', 'orange']
print(fruits)  # 出力: ['apple', 'banana', 'orange']

# タプル（変更不可）
point = (10, 20)
print(point)  # 出力: (10, 20)

# 辞書（キーと値のペア）
person = {'name': 'Taro', 'age': 25}
print(person['name'])  # 出力: Taro

# セット（重複なしの集合）
unique_nums = {1, 2, 2, 3}
print(unique_nums)  # 出力: {1, 2, 3}
